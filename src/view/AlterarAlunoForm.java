    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String telefone = txtTelefone.getText();
        String data = txtDataNascimento.getText();

        if (nome.isBlank() || email.isBlank()|| telefone.isBlank()|| data.isBlank()){
            JOptionPane.showMessageDialog(null,"Preencha todos os campos.");
        }else{
            Aluno a = new Aluno();
            a.setNome(nome);
            a.setEmail(email);
            a.setTelefone(telefone);
            a.setDataNascimento(data);
            int res = new AlunoDAO().atualizar(a);
            if ( res > 0 ){
                JOptionPane.showMessageDialog(null, "Registro alterado com sucesso!");
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Não foi possível realizar a operação.");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

