    private void configurarTabela(){
        DefaultTableModel m = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Nome");
        m.addColumn("Email");
        m.addColumn("Telefone");
        m.addColumn("Data de Nascimento");
        tabAlunos.setModel(m);
    }
    
    private void preencherTabela(List<Aluno> lista){
        configurarTabela();
        DefaultTableModel m = (DefaultTableModel)tabAlunos.getModel();
        for(Aluno a : lista){
            m.addRow(new Object[]{a.getId(),a.getNome(),a.getEmail(),a.getTelefone(),a.getDataNascimento()});
        }
        tabAlunos.setModel(m);
    }

