    private void txtPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarKeyReleased
        
        String chave = txtPesquisar.getText();
        
        if (chave.isEmpty()) {
            preencherTabela(new AlunoDAO().listar());
        } else {
            String filtro = cbxPesquisarPor.getSelectedItem().toString();
            
            if (filtro.equals("Id")) {
                preencherTabela(new AlunoDAO().pesquisarPorId(chave));
            } else if (filtro.equals("Nome")) {
                preencherTabela(new AlunoDAO().pesquisarPorNome(chave));
            } else if (filtro.equals("Email")) {
                preencherTabela(new AlunoDAO().pesquisarPorEmail(chave));
            } else if (filtro.equals("Telefone")) {
                preencherTabela(new AlunoDAO().pesquisarPorTelefone(chave));
            } else if (filtro.equals("Data de Nascimento")) {
                preencherTabela(new AlunoDAO().pesquisarPorData(chave));
            }
        }
        
    }//GEN-LAST:event_txtPesquisarKeyReleased

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        int linha = tabAlunos.getSelectedRow();
        
        if (linha >= 0) {
            int id = (int) tabAlunos.getValueAt(linha, 0);
            String nome = tabAlunos.getValueAt(linha, 1).toString();
            String email = tabAlunos.getValueAt(linha, 2).toString();
            String telefone = tabAlunos.getValueAt(linha, 3).toString();
            String dataNascimento = tabAlunos.getValueAt(linha, 4).toString();

            Aluno a = new Aluno(id, nome, email, telefone, dataNascimento);

            AlterarAluno alterar = new AlterarAluno(a);
            alterar.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um aluno na tabela para alterar.");
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

